public class PDFText {
    public static String name;
    public static String address;
    public static String email;
    public static String phoneNum;

    public static String university;
    public static String gradYear;
    public static String degree;
    public static String schoolLoc;

    public static String compName;
    public static String position;
    public static String workYears;
    public static String responsibilities;

    public PDFText(){
        name = "";
        address = "";
        email = "";
        phoneNum = "";
        university = "";
        gradYear = "";
        degree = "";
        schoolLoc = "";
        compName = "";
        position = "";
        workYears = "";
        responsibilities = "";

    }

    public void setName(String n) {name = n;}

    public void setAddress(String a) {address = a;}

    public void setEmail(String e) {email = e;}

    public void setPhoneNum(String num) {phoneNum = num;}

    public void setUniversity(String u) {university = u;}

    public void setGradYear(String y) {gradYear = y;}

    public void setDegree(String d) {degree = d;}

    public void setSchoolLoc(String loc) {schoolLoc = loc;}

    public void setCompName(String name) {compName = name;}

    public void setPosition(String pos) {position = pos;}

    public void setWorkYears(String yrs) {workYears = yrs;}

    public void setResponsibilities(String r) {responsibilities = r;}

}
